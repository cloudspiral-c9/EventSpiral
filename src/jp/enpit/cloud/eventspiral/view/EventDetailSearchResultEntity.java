package jp.enpit.cloud.eventspiral.view;

/**
 * <ol>
 *   <li>イベント情報を格納するエンティティクラス</li>
 * </ol>
 * @author s-egawa
 */

public class EventDetailSearchResultEntity extends AbstractEntity{

	/**
     * イベント情報
    */
	private java.util.List<EventDetailEntity> list;

	/**
     * イベント件数
    */
	private int	totalCount;

	/**
	 * デフォルトコンストラクタ
	 */
	public EventDetailSearchResultEntity(){
	}

	/**
	 * <ol>
         *   <li>イベント情報を取得する．</li>
	 * </ol>
	 * @return イベント情報（EventDetailEntityオブジェクト）のリスト
	 */

	public java.util.List<EventDetailEntity> getList() {
		return list;
	}

	/**
	 * <ol>
         *   <li>イベント情報をフィールドへ登録する．</li>
	 * </ol>
	 * @param list イベント情報（EventDetailEntityオブジェクト）のリスト
	 */
	public void setList(java.util.List<EventDetailEntity> list) {
		this.list = list;
	}

	/**
	 * <ol>
         *   <li>イベント件数を取得する．</li>
	 * </ol>
	 * @return イベント件数
	 */
	public int getTotalCount() {
		return totalCount;
	}

	/**
	 * <ol>
         *   <li>イベント件数をフィールドへ登録する．</li>
	 * </ol>
	 * @param totalCount イベント件数
	 */
	public void setTotalCount(int totalCount) {
		this.totalCount = totalCount;
	}
}
